function weaponModifierContribution(
    id: string,
    target: EquipmentModifierContributionTemplate["definition"]["target"],
    label: string,
    value: number,
): EquipmentModifierContributionTemplate {
    return {
        id,
        definition: { target, kind: "modifier" },
        editable: { label, value },
    }
}

