import type {
    EquipmentBurden,
    EquipmentDamageType,
    EquipmentRange,
    EquipmentTier,
    EquipmentTrait,
    NormalizedEquipmentModifierContributionTemplate,
} from "@/equipment/import/types"

export interface Weapon {
    id: string;
    name: string;
    tier: EquipmentTier;
    trait: EquipmentTrait;
    damageType: EquipmentDamageType;
    range: EquipmentRange;
    burden: EquipmentBurden;
    damage: string;
    featureName?: string;
    description?: string;
    modifierContributions: NormalizedEquipmentModifierContributionTemplate[];
}

function weaponModifierContribution(
    id: string,
    target: NormalizedEquipmentModifierContributionTemplate["definition"]["target"],
    label: string,
    value: number,
): NormalizedEquipmentModifierContributionTemplate {
    return {
        id,
        definition: { target, kind: "modifier" },
        editable: { label, value },
    }
}

